<h1>Aide</h1>
<div class="ui-body ui-body-a ui-corner-all">
	<h3>Comment jouer ?</h3>
	<p>Il suffit de vous rendre sur la page d'accueil, de taper un <i>pseudo</i> ainsi qu'un <i>mot de passe</i>, et de cliquer sur le bouton <strong>Jouer</strong>.<br>
		Il n'y a pas besoin de s'inscrire.</p>
	<br>
	<h3>Comment débuter le jeu ?</h3>
	<p>Dans un premier temps, il est courant d'améliorer la <i>scierie</i> afin d'augmenter la production de bois.<br>
		Ensuite, améliorer les <i>champs</i> puis un <i>poulailler</i> est une bonne chose. Il sera alors possible de recruter des poulets dans l'armée.<br>
		Après vous pourrez envoyer vos poulets dans différentes <i>missions</i>, ou <i>attaquer</i> les autres joueurs.</p>
	<br>
	<h3>Quel est le but ?</h3>
	<p>Être le premier joueur dans le classement en ayant la plus grande armée possible, en évitant au maximum les pertes.</p>
	<br>
	<h3>Pourquoi une attente ?</h3>
	<p>La production des ressources s'effectuent en temps-réel : le bois et les graines continuent d'augmenter même quand vous n'êtes pas connecté.<br>
		Suivant la stratégie choisie, des délais (parfois en heures) peuvent être nécessaires pour atteindre une quantité de ressources suffisantes à l'amélioration d'un bâtiment.</p>
	<br>
	<h3>Comment obtenir la Web App ?</h3>
	<p><strong>Poulet Army</strong> est aussi disponible sous la forme d'une Web App via une icône :</p>
	<ul>
		<li>Android : Chrome, icône options (en haut à droite), <i>Ajouter à l'écran d'accueil</i>.</li>
		<li>iPhone : Safari, icône partager, <i>Ajouter à l'écran d'accueil</i>.</li>
	</ul>
